import streamlit as st
import pandas as pd
from zeep import Client
from zeep.transports import Transport

# Ticimax WSDL ve yetki kodu
WSDL_URL = "https://www.ofis26.com/Servis/UrunServis.svc?wsdl"
UYE_KODU = "HVEKN1KK1USEAD0VAXTVKP8FWGN3AE"
CSV_YOLU = "pages/veri_kaynaklari/ana_urun_listesi.csv"

def satis_fiyatlarini_cek():
    try:
        client = Client(wsdl=WSDL_URL, transport=Transport(timeout=60))

        # Filtre nesnesi
        urun_filtresi = {
            "Aktif": -1,
            "Firsat": -1,
            "Indirimli": -1,
            "Vitrin": -1,
            "KategoriID": 0,
            "MarkaID": 0,
            "TedarikciID": -1,
            "ToplamStokAdediBas": 0,
            "ToplamStokAdediSon": 100,
            "UrunKartiID": 0
        }

        # Sayfalama nesnesi
        sayfalama = {
            "BaslangicIndex": 0,
            "KayitSayisi": 100,
            "SiralamaDegeri": "ID",
            "SiralamaYonu": "DESC"
        }

        sonuc = client.service.SelectUrun(
            UyeKodu=UYE_KODU,
            f=urun_filtresi,
            s=sayfalama
        )

        if not sonuc or not hasattr(sonuc, "UrunListesi") or not sonuc.UrunListesi:
            return None

        # Urun listesi â†’ pandas DataFrame'e Ã§evir
        urunler = pd.DataFrame([{
            "StokKodu": urun.StokKodu,
            "SatisFiyati": urun.Varyasyonlar[0].SatisFiyati if urun.Varyasyonlar else None
        } for urun in sonuc.UrunListesi])

        return urunler

    except Exception as e:
        st.error(f"ÃœrÃ¼n verisi Ã§ekilirken hata oluÅŸtu: {e}")
        return None

# Streamlit ArayÃ¼zÃ¼
st.title("ðŸ›’ Ticimax ÃœrÃ¼n FiyatlarÄ±nÄ± GÃ¼ncelle")

if st.button("ðŸ“¥ SatÄ±ÅŸ FiyatlarÄ±nÄ± Ticimax'ten Ã‡ek"):
    fiyat_df = satis_fiyatlarini_cek()

    if fiyat_df is None or fiyat_df.empty:
        st.warning("ÃœrÃ¼n listesi boÅŸ veya Ã§ekilemedi.")
    else:
        # Mevcut CSV'den Ã¼rÃ¼n verisi yÃ¼kle
        ana_df = pd.read_csv(CSV_YOLU)

        # Stok kodu ile eÅŸleÅŸtir
        ana_df = ana_df.merge(fiyat_df, how="left", on="StokKodu", suffixes=('', '_Yeni'))

        # Yeni fiyatlarÄ± gÃ¼ncelle
        ana_df["Ofis26 SatÄ±ÅŸ FiyatÄ±"] = ana_df["SatisFiyati"]

        st.success("SatÄ±ÅŸ fiyatlarÄ± baÅŸarÄ±yla gÃ¼ncellendi.")
        st.dataframe(ana_df)

        # Ä°stersen CSV'ye yaz:
        ana_df.to_csv(CSV_YOLU, index=False)
